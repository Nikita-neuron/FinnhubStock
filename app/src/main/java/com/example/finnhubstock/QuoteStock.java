package com.example.finnhubstock;

public class QuoteStock {
    private double c;

    private double h;

    private double l;

    private double o;

    private double pc;

    private int t;

    public void setC(double c){
        this.c = c;
    }
    public double getC(){
        return this.c;
    }
    public void setH(double h){
        this.h = h;
    }
    public double getH(){
        return this.h;
    }
    public void setL(double l){
        this.l = l;
    }
    public double getL(){
        return this.l;
    }
    public void setO(double o){
        this.o = o;
    }
    public double getO(){
        return this.o;
    }
    public void setPc(double pc){
        this.pc = pc;
    }
    public double getPc(){
        return this.pc;
    }
    public void setT(int t){
        this.t = t;
    }
    public int getT(){
        return this.t;
    }
}
